<h2>Die Physik ist nicht genug</h2>
<p>Nach diesem Motto sind wir von PhysikOnline diesen Sommer zu dem Schluss gekommen, dass es Zeit ist, unser mittlerweile seit fünf Jahren laufendes Videoprojekt thematisch zu erweitern. Geplant ist der journalistische Großangriff auf sämtliche Fachbereiche am Riedberg! Unzählige ProfessorInnen, Arbeitsgruppen usw. warten darauf, von motivierten StudentInnen interviewt und porträtiert zu werden. Praktischerweise werden wir bei dieser Idee auch von offizieller Seite motiviert, denn in der aktuellen SelF- Ausschreibung (Förderung studentischer eLearning-Initiativen an der Goethe-Universität) wurde unser Antrag auf finanzielle Unterstützung bewilligt. Mit der Erfahrung aus fünf Jahren Podcast-Wiki-Physik und den finanziellen Mitteln im Rücken wollen wir nun also loslegen.
</p>
<h3>Da gibt es nur ein kleines Problem.</h3>
<p>Als Physiker hält sich unser Wissen über andere Fachbereiche deutlich in Grenzen. Und an dieser Stelle kommst du ins Spiel:
Wenn du StudentIn einer der Fachbereiche 11-16 bist und Lust hast, vor oder hinter der Kamera zu arbeiten um deinen Fachbereich der Welt (oder zumindest interessierten frankfurter StudentInnen) durch kurze Videobeiträge zu präsentieren, bist du hier richtig.
Das Projekt kommt nicht nur den KommilitonInnen deines Fachbereichs zugute, sondern du hast im Rahmen des Projektes die Möglichkeit, deinen Fachbereich besser kennen zu lernen und über den „Tellerrand“ deines Fachs hinauszuschauen.
</p><p><b>Zusätzliches Schmankerl:</b> Dank der Unterstützung seitens der Universität, wirst du (falls du magst) für deine Arbeit sogar als HiWi bezahlt!
Deine Arbeitszeit kannst du dir quasi frei einteilen, geplant sind aber feste wöchentliche Gruppentreffen, um den Fortschritt der einzelnen Videoprojekte zu koordinieren, bzw. neue Ideen zu entwickeln.
</p>
<p>
<b>Technisches Vorwissen oder Erfahrung erwarten wir nicht.</b> Viel wichtiger: Eigene Ideen einbringen und die Bereitschaft, diese selbständig umzusetzen. 
