<h2>Kreatives Mitgestalten</h2>
PhysikOnline steht f&uuml;r die Idee, als Studenten an der Goethe Universit&auml;t das Studium der Physik mitzugestalten und kreativ zu erg&auml;nzen. Was darunter genau zu verstehen ist, ist eine zentrale Frage der wir regelm&auml;&szlig;ig in offenen Diskussionen nachgehen. In der Vergangenheit sind daraus ganz unterschiedliche Projekte hervorgegangen, wie das <b>Elearning System</b> des Fachbereichs, <b>PhysikOnline-TV</b> (Podcast-Wiki Physik) und die gerade in den Startl&uuml;chern stehende kollaborative Arbeits- und Lernplatform <b>POKAL</b>. In diesen Sparten spiegeln sich die diversen Interpretationen der oben genannten Zielsetzung wider, die ma&szlig;geblich durch die Zusammensetzung der Gruppe von Studenten hinter PhysikOnline bestimmt wird. Deshalb sind wir auf der Suche nach interessierten und motivierten Studenten, <b>wie Dir!</b> Aus jedem neuen Kopf entspringen frische Ideen, die diskutiert und umgesetzt werden wollen. PhysikOnline ist die studentische Platform, wo genau das realisiert werden kann!

<h2>Umsetzen von Ideen</h2>
Die Herausforderung ist dabei anf&auml;ngliche, wage Ideen zu konkretisieren und nach und nach in Taten zu &uuml;bersetzen.
Hierf&uuml;r sind meist auch Gelder (z.B. f&uuml;r Kameras, Videoschnittsoftware, Server, ...), R&auml;ume und andere Hilfe notwendig. Diese versuchen wir regelm&auml;&szlig;ig z.B. durch Vermarktung der Ideen in Vortr&auml;gen vor potentiellen Unterst&uuml;tzern und nutzen von F&ouml;rderungsprogrammen der Universit&auml;t und des Landes Hessen zu gewinnen. Zus&auml;tzlich k&ouml;nnen wir durch die mittlerweile langj&auml;hrige Integration in den Fachbereich auch von dort auf Unterst&uuml;zung hoffen. Ein erfolgreiches studentisches Zusammenarbeiten bei PhysikOnline setzt also nicht nur Kreativit&auml;t sondern auch Projektorganisation voraus!

<h2>Studenten</h2>
Es liegt in der Natur des Curriculums des Physikstudiums, dass es nach planm&auml;&szlig;ig f&uuml;nf (Master) Jahren endet. Da PhysikOnline ein ausschlie&szlig;lich studentisches Projekt ist, unterliegt es demnach zwangsl&auml;fig einem Schrumpfungsprozess, dem es entgegenzuwirken gilt. Nicht nur aus Diversit&auml;tsgr&uuml;nden sind demnach explizit motivierte <b>Studenten eines jeden Semesters</b> (und auch angrenzender Fachbereiche) herzlich zur Teilnahme eingeladen.
